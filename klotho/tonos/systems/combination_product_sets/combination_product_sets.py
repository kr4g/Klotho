  @classmethod
  def hexany(cls, factors: Tuple[int] = (1, 3, 5, 7), master_set='tetrad', normalized: bool = False):
    """
    Hexany -- a six-note CPS from 4 factors taken 2 at a time.

    Parameters
    ----------
    factors : tuple of int, optional
        Exactly 4 generator factors. Default is ``(1, 3, 5, 7)``.
    master_set : str or MasterSet, optional
        Geometric layout template. Default is ``'tetrad'``.
    normalized : bool, optional
        Normalize ratios by the largest factor. Default is False.

    Returns
    -------
    CombinationProductSet

    References
    ----------
    .. [1] https://en.wikipedia.org/wiki/Hexany
    .. [2] https://en.xen.wiki/w/Hexany
    """
    if len(factors) != 4:
      raise ValueError('Hexany requires exactly 4 factors.')
    return cls(factors, r=2, master_set=master_set, normalized=normalized)

